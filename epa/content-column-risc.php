	  	<h2>
	  		<span>EPAコラム</span>
	  		<p>血管に関わるリスク</p>
	  	</h2>

	  	<dl class="inner">
	  		<dt><img src="/wp-content/themes/xeory_base/img/img_column_risc_fv.png"></dt>
	  		<dd>
	  			<p>
					血管リスクとして誰もが恐れる<span class="txt_bold txt_line">心筋梗塞や脳梗塞</span>は、<span class="txt_bold txt_line">血管が詰まってしまう病気</span>です。<br>
					主な原因は<span class="txt_bold txt_line">「動脈硬化」</span>もしくは<span class="txt_bold txt_line">「血管の異常な収縮」</span>とされています。
	  			</p>
	  			<p>
					動脈硬化は、<span class="txt_bold txt_line">食事や喫煙・運動・ストレスなど長年の生活習慣の影響</span>が大きいと言われています。<br>
					生活習慣を改善すれば予防をすることができるので、心当たりがある方は改善を心がけましょう。<br>
	  			</p>
	  			<p>
					・<span class="txt_bold txt_line">肥満</span>を解消する<br>
					・<span class="txt_bold txt_line">動物性脂肪</span>を減らし、<span class="txt_bold txt_line">野菜</span>を増やす<br>
					・<span class="txt_bold txt_line">適度の運動</span>をとり入れる<br>
					・<span class="txt_bold txt_line">禁煙</span>を心がける<br>
					・<span class="txt_bold txt_line">EPAサプリ</span>を摂る<br>
	  			</p>
	  			<p>
					一方で、<span class="txt_bold txt_line">血管の異常な収縮</span>は、食事に気をつけていたり、適度に運動をしている健康な人にも前触れなく起こる可能性があります。<br>
					メカニズムは解明されているようですが、原因は分かっていません。<br>
					現在も世界中で研究をされていますが、現在分かっているのは<span class="txt_bold txt_line">「青魚に多く含まれているEPAサプリを摂取する」</span>ことが唯一予防方法ということです。
	  			</p>
	  			<p>
					ただし、「EPAとは」にも記載していますが、EPAが含まれる青魚は加熱などによりその立体構造が変わってしまい、EPAの効果が弱くなってしまうため、青魚と同じ立体構造を保ったままのEPAが配合されているかどうかが重要になります。<br>
					<span class="txt_bold txt_line">血管の異常収縮を予防されたい方は、EPAの精製法にも着目してサプリ</span>を選んでいただくことをオススメします。
	  			</p>
	  		</dd>
	  	</dl>