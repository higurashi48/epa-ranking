	  	<h2>
	  		<span>EPAコラム</span>
	  		<p>DHAとEPAの違い</p>
	  	</h2>

	  	<dl class="inner">
	  		<dt><img src="/wp-content/themes/xeory_base/img/img_column_dha_fv.png"></dt>
	  		<dd>
	  			<p><span class="txt_bold txt_line">「DHA」と「EPA」</span>という言葉を、一度は耳にしたことがあると思いますが、その違いを知っている方は少ないのではないでしょうか。<br>
EPAと同じくDHAも魚油に多く含まれている成分ですが、その働きには違いがあります。
				</p>
	  			<p>
					・DHAは<span class="txt_bold txt_line">「ドコサヘキサエン酸(docosahexaenoic acid)」の略。</span><br>
					<span class="txt_bold txt_line">マグロやカツオ・サンマ・サバなど</span>に多く含まれています。<br>
					頭の働きに高い効果があると知られており、<span class="txt_bold txt_line">記憶力や視力に対して効果が期待</span>できます。
	  			</p>
	  			<p>
					・EPA　<span class="txt_bold txt_line">「エイコサペンタエン酸(eicosapentaenoic acid)」の略。</span><br>
					<span class="txt_bold txt_line">イワシ・サバなど</span>に多く含まれています。<br>
					血栓をできにくくしたり、高脂血症を予防するので、<span class="txt_bold txt_line">脳梗塞や心筋梗塞、動脈硬化を予防する働き</span>があります。
	  			</p>
	  			<p>
	  				上記のように、<span class="txt_bold txt_line">DHAは記憶力や視力の脳に、EPAは血液の流れ</span>にといった違いがあるようなので、お悩みに合わせてサプリを選んでみてください。
	  			</p>


	  		</dd>
	  	</dl>