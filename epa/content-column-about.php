	  	<h2>
	  		<span>EPAコラム</span>
	  		<p>EPAについて</p>
	  	</h2>

	  	<dl class="inner">
	  		<dt><img src="/wp-content/themes/xeory_base/img/img_column_about_fv.png"></dt>
	  		<dd>
	  			<p>
				EPAは<span class="txt_bold txt_line">「エイコサペンタエン酸」</span>の略称で<span class="txt_bold txt_line">いわし・さばなどの青魚</span>に多く含まれています。<br>
				その働きが発見されて以来、血液を健康に保ち、<span class="txt_bold txt_line">血栓や高脂血症、動脈硬化や心筋梗塞、脳梗塞を予防するという働きがある</span>ということが世界中の医学者によって研究され続けてきました。
	  			</p>
				<p>
					EPAは体に欠かせない<span class="txt_bold txt_line">「オメガ3系脂肪酸」</span>として、厚生労働省では<span class="txt_bold txt_line">1日に1000㎎の摂取を推奨</span>していますが、食事で1日1000㎎補うのは困難です。<br>
					また、EPAが含まれる青魚は加熱などによりその立体構造が変わってしまい、EPAの効果が弱くなってしまうことが分かっているので、<span class="txt_bold txt_line">サプリメントでEPAを摂取する方法がおすすめ</span>です。<br>一度に大量のEPAを摂取するよりも毎日こまめに摂取することで、効果を実感した方も多いようなので、<span class="txt_bold txt_line">継続して飲むこと</span>が大切です。
				</p>
				<p>
					しかし、一口にサプリといっても色々な種類があり、EPAをメインにしているものから、DHAやその他の健康成分を配合したものまで様々です。<br>
					<span class="txt_bold txt_line">EPAの含有量も異なる</span>ので、いろいろ比較した上でお悩みに合ったものを選ぶといいでしょう。
				</p>
	  		</dd>
