	  			<p>
					サプリメントを選ぶ上で、ポイントとなる項目をご紹介します。
	  			</p>
	  			<p>
				<span class="txt_bold txt_line">・EPAの量が多く配合されているか？</span><br>
				商品によって、EPA以外にもDHAなどその他の成分を配合しているものもあるので、純粋にEPAのサプリを飲みたい場合は、<span class="txt_bold txt_line">EPAがどれだけ配合されているのか</span>を確認することをおすすめします。
	  			</p>
	  			<p>
					<span class="txt_bold txt_line">・EPAの抽出にこだわっているか？</span><br>
					EPAならどれでもいい！というわけではありません・・・。「EPAとは」にも記載しましたが、EPAが多く含まれる青魚は、加熱などによりその立体構造が変わってしまい、EPAの効果が弱くなってしまうことが分かっています。精製・加工の際にパワーが失われやすいため<span class="txt_bold txt_line">EPAの抽出にこだわったもの</span>を選びましょう。
	  			</p>
	  			<p>
					<span class="txt_bold txt_line">・EPA（油）を体内に吸収しやすくなっているか？</span><br>
					魚油を摂取するには吸収効率も大事なポイントです。<span class="txt_bold txt_line">年齢とともに消化器官の働きも落ちてくると言われている</span>ので、せっかくいい成分を摂取しても、実は体に吸収されていなかった・・・ということも考えられます。<br>特にEPAは油なので、<span class="txt_bold txt_line">体内に吸収しやすくなる成分の配合・加工がされていたら、より効果を実感しやすい</span>かもしれません。
	  			</p>
	  			<p>
					<span class="txt_bold txt_line">・安心できるサプリかどうか？</span><br>
					サプリメントは医薬品ではないので、公式サイトなどで効能を表示することはできません。
					ただし、インターネットで商品名や成分名を検索すると、<span class="txt_bold txt_line">研究の実績や口コミが出てくる</span>商品もあるので、比較をして信頼度を判断しましょう。
	  			</p>
